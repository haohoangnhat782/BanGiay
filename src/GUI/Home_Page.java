    private void btExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExportActionPerformed
        if(lbHienTai.getText().equals("Supplier")){
            SupplierBUS supplierBUS = new SupplierBUS();
            supplierBUS.ExportExcel();
            JOptionPane.showMessageDialog(this,"export excel file successfully");
            return;
        }
        if(lbHienTai.getText().equals("Employee")){
            EmployeeBUS employeeBUS = new EmployeeBUS();
            employeeBUS.ExportExcel();
            JOptionPane.showMessageDialog(this,"export excel file successfully");
            return;
        }
        if(lbHienTai.getText().equals("Product")){
            ProductBUS productBUS = new ProductBUS();
            productBUS.ExportExcel();
            JOptionPane.showMessageDialog(this,"export excel file successfully");
            return;
        }
        if(lbHienTai.getText().equals("Customer")){
            CustomerBUS customerBUS = new CustomerBUS();
            customerBUS.ExportExcel();
             JOptionPane.showMessageDialog(this,"export excel file successfully");
            return;
        }
         
        
    }//GEN-LAST:event_btExportActionPerformed

