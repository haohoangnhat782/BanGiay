    public void openMenuBar() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = 0; i < DoDai; i++) {
                    pnMenu.setSize(220, i);
                    try {
                        Thread.sleep(1);
                    } catch (Exception ex) {
                        System.out.println(ex);
                    }
                }
            }

        }).start();
    }

    public void closeMenuBar() {
        new Thread(new Runnable() {
            @Override
            public void run() {
                for (int i = DoDai; i > 0; i--) {
                    pnMenu.setSize(220, i);
                    try {
                        Thread.sleep(0);
                    } catch (Exception ex) {
                        System.out.println(ex);
                    }
                }
            }

        }).start();
    }
