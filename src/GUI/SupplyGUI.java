    public void setSoLuong(CTSupplyDTO ct){
        for(ProductDTO pro : listProduct){
            if(pro.getMaSP().equals(ct.getMaSP())){
                SL = Integer.parseInt(pro.getSoLuong()) + Integer.parseInt(ct.getSoLuong());
                ProductDTO p = new ProductDTO(
                        pro.getMaSP(), 
                        pro.getTenSP(), 
                        String.valueOf(SL), 
                        pro.getGiaNhapHang(), 
                        pro.getGiaSanPham(), 
                        pro.getMaLoai(), 
                        pro.getMaNSX(), 
                        pro.getIMG());
                proBUS.update(p);
            }
        }
    }
