    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        String sql ="SELECT * FROM NHAPHANG WHERE 1 = 1";
        try{
            String MaNH = txtMaNH.getText();
            String MaNCC= txtMaNCC.getText();
            String MaNV = txtMaNV.getText();
            Date dateNgayNhap = txtNgayNhap.getDate();
            Date dateNgayBatDau = txtNgayBatDau.getDate();
            Date dateNgayKetThuc = txtNgayKetThuc.getDate();
            String NgayLap = "";
            String NgayBatDau ="";
            String NgayKetThuc = "";
            String TongTien = txtTongTien.getText();
            if(ckMaNH.isSelected()){
                if(MaNH.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Chưa nhập mã nhập hàng cần tìm");
                    return;
                }
                sql +=" AND MANH LIKE " + MaNH ;
            }
            if(ckMaNCC.isSelected()){
                if(MaNCC.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Chưa nhập mã nhà cung cấp cần tìm");
                    return;
                }
                sql +=" AND MANCC LIKE '%" + MaNCC + "%'";
            }
            if(ckMaNV.isSelected()){
                if(MaNV.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Chưa nhập mã nhân viên cần tìm");
                    return;
                }
                sql +=" AND MANV LIKE '%" + MaNV + "%'";
            }
            if(NgayNhap.isSelected() && dateNgayNhap != null){
                NgayLap = dateFormat.format(dateNgayNhap);
                sql +=" AND NGAYNHAP = '" + NgayLap + "'";
            }
            if(CkTimNgayTheoKhoang.isSelected() && dateNgayBatDau != null && dateNgayKetThuc != null){
                NgayBatDau = dateFormat.format(dateNgayBatDau);
                NgayKetThuc = dateFormat.format(dateNgayKetThuc);
                sql +=" AND NGAYNHAP BETWEEN '" + NgayBatDau +"' AND '" + NgayKetThuc +"'";
            }
            if(ckTongTien.isSelected()){
                if(TongTien.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Chưa nhập tổng tiền thấp nhất cần lọc");
                    return;
                }
                sql +=" AND TONGTIEN >= " + TongTien + " AND TONGTIEN <= 1000000000";
            }
            loadSearch(sql);
        }
        catch(Exception ex){
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btSearchActionPerformed

