    public void showAllSupply(ArrayList<SupplyDTO> listSup){
        modelSupply.setRowCount(0);
        for(int i = 0; i < listSup.size();i++){
            modelSupply.addRow(new String[]{
            listSup.get(i).getMaNH(),
            dateFormat.format(listSup.get(i).getNgayNhap()),
            listSup.get(i).getMaNV(),
            listSup.get(i).getMaNCC(),
            listSup.get(i).getTongTien()
            
            });
        }
    }
